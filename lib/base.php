<?php #=========================================================================

#-------------------------------------------------------------------------------
error_reporting(E_ALL | E_STRICT);

#-------------------------------------------------------------------------------
function getGet($id, $default) {
	return isset($_GET[$id]) ? $_GET[$id] : $default;
}

#-------------------------------------------------------------------------------
function getPost($id, $default) {
	return isset($_POST[$id]) ? $_POST[$id] : $default;
}

#-------------------------------------------------------------------------------
function getServer($id, $default) {
	return isset($_SERVER[$id]) ? $_SERVER[$id] : $default;
}

#============================================================================ ?>
